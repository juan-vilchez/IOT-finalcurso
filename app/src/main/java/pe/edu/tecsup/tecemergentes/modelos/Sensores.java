package pe.edu.tecsup.tecemergentes.modelos;

public class Sensores {

    private int gas;
    private int sonido;

    public Sensores() {
    }

    public int getGas() {
        return gas;
    }

    public void setGas(int gas) {
        this.gas = gas;
    }

    public int getSonido() {
        return sonido;
    }

    public void setSonido(int sonido) {
        this.sonido = sonido;
    }
}
